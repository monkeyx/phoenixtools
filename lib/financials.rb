	def self.split_out_poslist
		list = []
		POS_LIST.each do |line|
			id = line[0][line[0].rindex("(")+1..line[0].rindex(")")-1]
			list << {'id' => id, 'position' => line[0], 'type' => line[1], 'stardate' => line[2], 'stellars' => line[3]}
		end
		list
	end

	def self.export_financials!
		list = Financials.split_out_poslist
		File.open('financials.csv', 'w') do |f|
			array = ["ID", "Name", "Star System", "Celestial Body", "Positition Type", "Transaction Type", "Stardate", "Stellars"]
			f.write(array.join(",") + "\n")
			list.each do |line|
				id = line['id']
				p = Position.find_by_id(id)
				array = [id]
				if p
					array = array + [p.name, p.star_system.to_s, p.celestial_body.to_s, p.position_class]
				else
					array = array + [line['position'], "", "", ""]
					puts "Position #{id} not found"
				end
				array = array + [line['type'], line['stardate'], line['stellars'].gsub(",","")]
				f.write(array.join(",") + "\n")
			end
		end
	end

